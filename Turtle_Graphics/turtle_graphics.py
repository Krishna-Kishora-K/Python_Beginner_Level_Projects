from turtle import *
t1=Turtle()
def square():
    t1.forward(200)
    t1.left(90)
    t1.forward(200)
    t1.left(90)
    t1.forward(200)
    t1.left(90)
    t1.forward(200)
def triangle():
    t1.left(90)
    t1.forward(250)
    t1.left(120)
    t1.forward(250)
    t1.left(120)
    t1.forward(250)
def pentagonal():
    t1.left(120)
    t1.forward(150)
    t1.left(72)
    t1.forward(150)
    t1.left(72)
    t1.forward(150)
    t1.left(72)
    t1.forward(150)
    t1.left(72)
    t1.forward(150)

t1.penup()
t1.goto(-250,150)
t1.pendown()
t1.color('red')
t1.begin_fill()
t1.circle(100)
t1.end_fill()
t1.penup()
t1.goto(200,150)
t1.pendown()
t1.color('green')
t1.begin_fill()
square()
t1.end_fill()
t1.penup()
t1.goto(-400,-200)
t1.pendown()
t1.color('orange')
t1.begin_fill()
triangle()
t1.end_fill()
t1.penup()
t1.goto(200,-200)
t1.pendown()
t1.color('blue')
t1.begin_fill()
pentagonal()
t1.end_fill()

t1.screen.mainloop()
